package dsynhub.its.bean;

public class OSBean {
private int osid;
private String os;
public OSBean() {
	this.osid = 0;
	this.os = null;
}
public int getOsid() {
	return osid;
}
public void setOsid(int osid) {
	this.osid = osid;
}
public String getOs() {
	return os;
}
public void setOs(String os) {
	this.os = os;
}

}
